    public void updateTable() {
        DefaultTableModel tabela = (DefaultTableModel) consultasTable.getModel();
        tabela.setRowCount(0);
        ArrayList lista = daoConsulta.list();
        Iterator it = lista.iterator();
        while (it.hasNext()) {
            TableCellConsulta consulta = (TableCellConsulta) it.next();
            tabela.addRow(new Object[]{consulta.getId(), consulta.getMedico(), consulta.getPaciente(), consulta.getConvenio()});
        }
    }

    public void clean() {
        txtId.setText("");
        txtConv.setText("");
        btnExcluir.setEnabled(false);

        listMedicos.removeAllItems();
        ArrayList arrayMed = daoMedico.list();
        for (int counter = 0; counter < arrayMed.size(); counter++) {
            Medico medico = (Medico) arrayMed.get(counter);
            listMedicos.addItem(Integer.toString(medico.getId()) + " - " + medico.getNome());
        }

        listPacientes.removeAllItems();
        ArrayList arrayPac = daoPaciente.list();
        for (int counter = 0; counter < arrayPac.size(); counter++) {
            Paciente paciente = (Paciente) arrayPac.get(counter);
            listPacientes.addItem(Integer.toString(paciente.getId()) + " - " + paciente.getNome());
        }
    }

