    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        if (txtConv.getText().trim().isEmpty() || listMedicos.getSelectedItem() == null || listPacientes.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(rootPane, "PREENCHA TODOS OS CAMPOS");
        } else {
            Consulta con = new Consulta();
            con.setConvenio(txtConv.getText().trim());

            String item = (String) listMedicos.getSelectedItem();
            con.setIdMedico(Integer.parseInt(item.split(" - ")[0]));
            String pacienteItem = (String) listPacientes.getSelectedItem();
            con.setIdPaciente(Integer.parseInt(pacienteItem.split(" - ")[0]));

            String id = txtId.getText().trim();
            String msg;
            if (id.isEmpty()) {
                msg = daoConsulta.create(con);
                if (msg.equals("1")) {
                    msg = "CADASTRO EFETUADO COM SUCESSO";
                }
                JOptionPane.showMessageDialog(rootPane, msg);
            } else {
                con.setId(Integer.parseInt(id));
                msg = daoConsulta.update(con);
                if (msg.equals("1")) {
                    msg = "ALTERAÇÃO EFETUADA COM SUCESSO";
                }
                JOptionPane.showMessageDialog(rootPane, msg);
            }
            this.clean();
            this.updateTable();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void consultasTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_consultasTableMouseClicked
        // TODO add your handling code here:
        String id = consultasTable.getValueAt(consultasTable.getSelectedRow(), 0).toString();
        String medicoLabel = consultasTable.getValueAt(consultasTable.getSelectedRow(), 1).toString();
        String pacienteLabel = consultasTable.getValueAt(consultasTable.getSelectedRow(), 2).toString();
        String convenio = consultasTable.getValueAt(consultasTable.getSelectedRow(), 3).toString();

        txtId.setText(id);
        listMedicos.setSelectedItem(medicoLabel);
        listPacientes.setSelectedItem(pacienteLabel);
        txtConv.setText(convenio);

        btnExcluir.setEnabled(true);
    }//GEN-LAST:event_consultasTableMouseClicked

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        int confirm = JOptionPane.showConfirmDialog(rootPane, "DESEJA EXCLUIR O REGISTRO?");
        if (confirm == 0) {
            String msg = daoConsulta.delete(Integer.parseInt(txtId.getText()));
            if (msg.equals("1")) {
                msg = "EXCLUSÃO EFETUADA COM SUCESSO";
            }
            JOptionPane.showMessageDialog(rootPane, msg);
            this.clean();
            this.updateTable();
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

